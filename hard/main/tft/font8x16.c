#include "font8x16.h"
#include <stddef.h>

static const uint8_t FONT8x16_a[16] = {
    0x00,0x00,0x00,0x00,
    0x3C,0x06,0x3E,0x66,
    0x66,0x3E,0x00,0x00,
    0x00,0x00,0x00,0x00
};

static const uint8_t FONT8x16_b[16] = {
    0x00,0x00,0x60,0x60,
    0x7C,0x66,0x66,0x66,
    0x66,0x7C,0x00,0x00,
    0x00,0x00,0x00,0x00
};

static const uint8_t FONT8x16_d[16] = {
    0x00,0x00,0x06,0x06,
    0x3E,0x66,0x66,0x66,
    0x66,0x3E,0x00,0x00,
    0x00,0x00,0x00,0x00
};

const uint8_t* font8x16_get(char c)
{
    switch (c) {
        case 'a': return FONT8x16_a;
        case 'b': return FONT8x16_b;
        case 'd': return FONT8x16_d;
        default:  return NULL;
    }
}