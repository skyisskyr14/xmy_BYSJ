const uint8_t* hz16x16_get_utf8(const char *hz)
{
    if (!hz) return NULL;

    for (size_t i = 0; i < sizeof(hz_table)/sizeof(hz_table[0]); i++)
    {
        if (hz_table[i].utf8[0] == (uint8_t)hz[0] &&
            hz_table[i].utf8[1] == (uint8_t)hz[1] &&
            hz_table[i].utf8[2] == (uint8_t)hz[2])
        {
            return hz_table[i].bitmap;
        }
    }

    return NULL;
}